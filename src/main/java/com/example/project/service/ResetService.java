    private final UserRepository userRepository;
    private final PasswordResetTokenRepository passwordResetTokenRepository;
    private final PasswordEncoder passwordEncoder;

    @Value("${project.host}")
    private String host;

    @Transactional
    public void resetPassword(String email, AppUser user) {
        String token = UUID.randomUUID().toString();

        String link = "http://" + host + "/resetPassword?token=" + token;

        PasswordResetToken passwordResetToken = new PasswordResetToken(token, user, LocalDateTime.now().plusMinutes(15));
        passwordResetTokenRepository.save(passwordResetToken);

        try {
            emailSender.send(email, buildEmail(user.getFirstName(), link), "Reset Password");
        } catch (Exception ignored) {}
    }

    public boolean validate(PasswordResetToken passwordResetToken) {

        return !passwordResetToken.getExpiryDate().isBefore(LocalDateTime.now());
    }

    public boolean reset(ResetRequest resetRequest, BindingResult bindingResult, RedirectAttributes redirectAttributes) {

        Optional<AppUser> user = userRepository.findByEmail(resetRequest.getEmail());

        if (user.isEmpty()){
            return false;
        }

        if (bindingResult.hasErrors()) {
            List<String> errors;

            List<ObjectError> allErrors = bindingResult.getAllErrors();
            errors = Arrays.stream(Objects.requireNonNull(allErrors.get(0).getDefaultMessage()).split(",")).toList();

            redirectAttributes.addFlashAttribute("login_info", errors);

            return false;
        }

        if (!resetRequest.getPassword2().equals(resetRequest.getPassword1())){
            redirectAttributes.addFlashAttribute("login_info", "Enter the same password");
            return false;
        }


        userRepository.changePassword(user.get().getEmail(), passwordEncoder.encode(resetRequest.getPassword1()));
        return true;
    }

